// и возвращает значение этого элемента или же указание, что такого элемента нет.

//Например, задан массив:
//1 4 7 2
//5 9 2 3
//8 4 2 4
//[1, 7] -> на этой позиции числа в массиве нет




        // Задаем двумерный массив
        int[,] array = {
            { 1, 4, 7, 2 },
            { 5, 9, 2, 3 },
            { 8, 4, 2, 4 }
        };

        // Ввод позиции элемента
        Console.Write("Введите позицию элемента (строка): ");
        int row = int.Parse(Console.ReadLine());

        Console.Write("Введите позицию элемента (столбец): ");
        int column = int.Parse(Console.ReadLine());

        // Проверка валидности позиции
        if (row >= 0 && row < array.GetLength(0) && column >= 0 && column < array.GetLength(1))
        {
            // Получение значения элемента
            int value = array[row, column];
            Console.WriteLine("Значение элемента: " + value);
        }
        else
        {
            Console.WriteLine("На указанной позиции нет элемента в массиве.");
        }
    
