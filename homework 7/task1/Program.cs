//Задача 47. Задайте двумерный массив размером m×n, заполненный случайными вещественными числами.

//m = 3, n = 4.
//0,5 7 -2 -0,2
//1 -3,3 8 -9,9
//8 7,8 -7,1 9



    
        // Задаем размеры массива
        int m = 3; // Количество строк
        int n = 4; // Количество столбцов

        // Создаем двумерный массив
        double[,] array = new double[m, n];

        // Создание объекта генератора случайных чисел
        Random random = new Random();

        // Заполнение массива случайными вещественными числами
        for (int i = 0; i < m; i++)
        {
            for (int j = 0; j < n; j++)
            {
                // Генерация случайного вещественного числа от -10 до 10
                array[i, j] = random.NextDouble() * 20 - 10;
            }
        }

        // Вывод массива на экран
        Console.WriteLine("Двумерный массив:");
        for (int i = 0; i < m; i++)
        {
            for (int j = 0; j < n; j++)
            {
                Console.Write(array[i, j] + "\t");
            }
            Console.WriteLine();
        }
    
