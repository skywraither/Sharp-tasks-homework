//Задача 56: Задайте прямоугольный двумерный массив. Напишите программу, которая будет находить строку с наименьшей суммой элементов.

//Например, задан массив:
//1 4 7 2
//5 9 2 3
//8 4 2 4
//5 2 6 7
//Программа считает сумму элементов в каждой строке и выдаёт номер строки с наименьшей суммой элементов: 1 строка







        // Задаем прямоугольный двумерный массив
        int[,] array = {
            { 1, 4, 7, 2 },
            { 5, 9, 2, 3 },
            { 8, 4, 2, 4 },
            { 5, 2, 6, 7 }
        };

        int rows = array.GetLength(0); // Количество строк
        int columns = array.GetLength(1); // Количество столбцов

        int minSumRow = 0; // Индекс строки с наименьшей суммой элементов
        int minSum = int.MaxValue; // Переменная для хранения минимальной суммы, инициализируем максимальным значением

        // Поиск строки с наименьшей суммой элементов
        for (int i = 0; i < rows; i++)
        {
            int sum = 0; // Сумма элементов текущей строки

            for (int j = 0; j < columns; j++)
            {
                sum += array[i, j];
            }

            // Если текущая сумма меньше минимальной, обновляем значение минимальной суммы и индекс строки
            if (sum < minSum)
            {
                minSum = sum;
                minSumRow = i;
            }
        }

        // Вывод строки с наименьшей суммой элементов
        Console.WriteLine("Строка с наименьшей суммой элементов: " + (minSumRow + 1) + " строка");
    
