// Задайте массив вещественных чисел. Найдите разницу между максимальным и 
// минимальным элементов массива.
// [3.22, 4.2, 1.15, 77.15, 65.2] => 77.15 - 1.15 = 76


        // Создание массива вещественных чисел
        double[] array = { 3.22, 4.2, 1.15, 77.15, 65.2 };

        // Инициализация переменных для минимального и максимального элементов
        double min = array[0];
        double max = array[0];

        // Поиск минимального и максимального элементов массива
        for (int i = 1; i < array.Length; i++)
        {
            if (array[i] < min)
            {
                min = array[i];
            }
            if (array[i] > max)
            {
                max = array[i];
            }
        }

        // Вычисление разницы между максимальным и минимальным элементами
        double difference = max - min;

        // Вывод результата
        Console.WriteLine("Массив: " + string.Join(", ", array));
        Console.WriteLine("Разница между максимальным и минимальным элементами: " + difference);