// Задайте массив заполненный случайными положительными трёхзначными числами.
// Напишите программу, которая покажет количество чётных чисел в массиве.
// [345, 897, 568, 234] -> 2 


        // Создание объекта генератора случайных чисел
        Random random = new Random();

        // Создание массива и заполнение случайными трехзначными числами
        int[] array = new int[4];
        for (int i = 0; i < array.Length; i++)
        {
            array[i] = random.Next(100, 1000); // Генерация случайного трехзначного числа
        }

        // Подсчет количества четных чисел в массиве
        int evenCount = 0;
        foreach (int number in array)
        {
            if (number % 2 == 0)
            {
                evenCount++;
            }
        }

        // Вывод результата
        Console.WriteLine("Массив: " + string.Join(", ", array));
        Console.WriteLine("Количество четных чисел: " + evenCount);
